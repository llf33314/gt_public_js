// 检查字符串是否为合法IP地址
export default str => {
  // 1.1.1.1  四段  [0 , 255]
  // 第一段不能为0
  // 每个段不能以0开头
  //
  // 本机IP: 58.50.120.18 湖北省荆州市 电信
  var reg = /^([1-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])(\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])){3}$/gi
  if (reg.test(str)) {
    return true
  } else {
    return false
  }
}
